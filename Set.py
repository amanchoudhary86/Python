# A set is an unordered collection of unique elements. This data type that allows you to store a group of elements without duplicates.
# A set is a collection which is both unordered and unindexed.
# Sets are written with curly brackets '{}'
# Sets are immutable, meaning that we cannot change the items after the set has been created.
# Sets are unordered, and every value which is added to a set is unique, and this means no duplicate values are allowed.

